#ifndef INT_H
#define INT_H

/* 8-bit */
typedef signed char			i8;
typedef unsigned char		u8;

/* 16-bit */
typedef short 				i16;
typedef unsigned short		u16;

/* 32-bit */
typedef int					i32;
typedef unsigned int		u32;

/* 64-bit */
typedef long long			i64;
typedef unsigned long long	u64;

/* alternate typedefs */
typedef u8	byte;
typedef	u16	hword;
typedef	u32	word;
typedef	u64	dword;

#endif
